function bang() {
	var idx = Math.floor(Math.random() * presets.length);
	output(idx);
}

function msg_int(idx) {
	if (inlet !== 1) { return; }
	idx = Math.max(0, Math.min(presets.length - 1, idx));
	output(idx);
}

// Max JS outputs integers as OSC int type — adding a tiny fraction forces float type tag
function f(n) { return n + 0.0000001; }

function output(idx) {
	var p = presets[idx];
	var out = [
		"ff0", f(p.freq[0]), "ff1", f(p.freq[1]), "ff2", f(p.freq[2]), "ff3", f(p.freq[3]), "ff4", f(p.freq[4]),
		"fr0", p.reso[0],    "fr1", p.reso[1],    "fr2", p.reso[2],    "fr3", p.reso[3],    "fr4", p.reso[4],
		"fa0", p.amp[0],     "fa1", p.amp[1],     "fa2", p.amp[2],     "fa3", p.amp[3],     "fa4", p.amp[4]
	];
	outlet(1, p.name);
	outlet.apply(this, [0].concat(out));
}
