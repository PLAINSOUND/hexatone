function ratioPartsToText([numerator, denominator]) {
  return denominator === 1 ? `${numerator}` : `${numerator}/${denominator}`;
}

function defaultToleranceCents(cents) {
  if (cents === 0 || cents === 1200) return 3;
  if (cents < 250 || cents > 950) return 8;
  if (cents < 500 || cents > 700) return 9;
  return 10;
}

function defaultWeightFromRatio([numerator, denominator]) {
  const complexity = numerator + denominator;
  return Math.max(0.45, Math.min(1, 1.15 - complexity / 40));
}

export const DEFAULT_TUNEABLE_INTERVALS = RAW_TUNEABLE_INTERVALS.map(([parts, cents]) => ({
  ratio: ratioPartsToText(parts),
  ratioParts: [...parts],
  cents,
  toleranceCents: defaultToleranceCents(cents),
  weight: defaultWeightFromRatio(parts),
}));

export const DEFAULT_INTERVAL_NEIGHBORHOOD_CHAIN = RAW_INTERVAL_NEIGHBORHOOD_CHAIN.map((parts) =>
  ratioPartsToText(parts),
);

export const DEFAULT_OVERTONAL_SOURCE_SET = RAW_OVERTONAL_SOURCE_SET.map((parts) =>
  ratioPartsToText(parts),
);

export function buildTuneableIntervalIndex(intervals = DEFAULT_TUNEABLE_INTERVALS) {
  return new Map(intervals.map((entry, index) => [entry.ratio, { ...entry, index }]));
}

export function findTuneableFamilyMatches(
  cents,
  { intervals = DEFAULT_TUNEABLE_INTERVALS, toleranceTable = null } = {},
) {
  return intervals
    .map((entry) => {
      const tolerance = toleranceTable?.[entry.ratio] ?? entry.toleranceCents;
      const deviation = Math.abs(cents - entry.cents);
      if (deviation > tolerance) return null;
      return {
        ...entry,
        tolerance,
        deviation,
        score: entry.weight * (1 - deviation / Math.max(tolerance, 0.001)),
      };
    })
    .filter(Boolean)
    .sort((a, b) => b.score - a.score);
}

export function getNeighborFamilies(
  ratioText,
  {
    chain = DEFAULT_INTERVAL_NEIGHBORHOOD_CHAIN,
    radius = 2,
  } = {},
) {
  const index = chain.indexOf(ratioText);
  if (index === -1) return [];
  const start = Math.max(0, index - radius);
  const end = Math.min(chain.length, index + radius + 1);
  return chain.slice(start, end).filter((ratio) => ratio !== ratioText);
}

export function findOvertonalNeighbors(
  ratioText,
  {
    sourceSet = DEFAULT_OVERTONAL_SOURCE_SET,
    chain = DEFAULT_INTERVAL_NEIGHBORHOOD_CHAIN,
    radius = 3,
  } = {},
) {
  const source = new Set(sourceSet);
  const chainNeighbors = getNeighborFamilies(ratioText, { chain, radius }).filter((ratio) => source.has(ratio));
  if (chainNeighbors.length) return chainNeighbors;

  const index = sourceSet.indexOf(ratioText);
  if (index === -1) return [];
  const start = Math.max(0, index - radius);
  const end = Math.min(sourceSet.length, index + radius + 1);
  return sourceSet.slice(start, end).filter((ratio) => ratio !== ratioText);
}
